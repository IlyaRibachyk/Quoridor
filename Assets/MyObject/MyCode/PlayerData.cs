using System.Collections.Generic;
using UnityEngine;

// Атрибут [System.Serializable] дозволяє Unity відображати поля цього класу 
// в Інспекторі, щоб ми могли налаштовувати їх прямо в редакторі.
[System.Serializable]
public class PlayerData
{
    // Посилання на 3D-модель (фішку) самого гравця на полі
    public GameObject playerObj;

    // Список стін, які знаходяться в "запасі" гравця (зазвичай відображаються на краю дошки).
    // Коли гравець ставить стіну, одна з цих стін зникає.
    public List<GameObject> wallStock;

    // Список цільових клітинок. Якщо фішка гравця стає на будь-яку з цих клітинок — він перемагає.
    public List<GameObject> goals = new List<GameObject>();

    // Список точок (Transform), де може знаходитися камера під час ходу цього гравця.
    // Використовується для перемикання ракурсів (наприклад, вигляд з-за спини).
    public List<Transform> cameraPositions;

    // ОЦЕЙ РЯДОК БУВ ПРОПУЩЕНИЙ:
    // Посилання на логіку (стратегію) гравця. 
    // Якщо це поле порожнє (null), значить цим гравцем керує реальна людина (через InputController).
    // Якщо тут є скрипт (наприклад, AIBotStrategy), значить це бот, і GameManager викличе його логіку.
    public IPlayerStrategy strategy;
}

// Клас, що описує один конкретний хід у грі (використовується здебільшого для ШІ / ботів)
public class Move
{
    // Чи є цей хід встановленням стіни? (якщо false, значить це переміщення фішки)
    public bool isWall;

    // --- Параметри для ходу-СТІНИ ---
    // Позиція якоря, куди ставиться стіна
    public Vector3 position;
    // Орієнтація стіни (true = вертикальна, false = горизонтальна)
    public bool vertical;

    // --- Параметри для ходу-ПЕРЕМІЩЕННЯ ---
    // Клітинка, на яку фішка має переміститися
    public GameObject targetTile;

    // --- Параметри для ВІДКАТУ ходу (потрібно алгоритму Minimax для симуляції майбутнього) ---
    // Звідки фішка почала рух (щоб можна було її повернути назад)
    public Vector3 previousPosition;
    // Список зв'язків між клітинками (ребер графа), які розірвала ця стіна.
    // Зберігаємо їх, щоб відновити граф після симуляції цього ходу.
    public List<GameObject[]> removedEdges = new List<GameObject[]>();
}

// Інтерфейс (контракт), який гарантує, що будь-який клас-стратегія
// матиме метод ExecuteTurn(). Це дозволяє GameManager'у не перейматися тим, який саме бот перед ним.
public interface IPlayerStrategy
{
    // Метод, який викликається GameManager-ом, коли настає черга цього гравця/бота діяти
    void ExecuteTurn();
}
