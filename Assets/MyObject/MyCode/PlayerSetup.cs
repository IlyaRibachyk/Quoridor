using UnityEngine;
using UnityEngine.UI;

// Клас, який налаштовує зовнішній вигляд фішки гравця при старті сцени.
// Він бере дані з GameSettings (які ми обрали в меню) і фарбує фішку у відповідний колір.
public class PlayerSetup : MonoBehaviour
{
    // Прапорці для визначення того, якому саме гравцю належить ця фішка.
    // Налаштовується вручну в Інспекторі Unity (галочка стоїть навпроти потрібного гравця).
    public bool isPlayer1;
    public bool isPlayer2; // Додайте це поле в інспекторі для другої фішки

    // Масив усіх доступних 3D-матеріалів (текстур дерева/кольорів), 
    // які відповідають спрайтам у Головному меню.
    public Material[] allWoodMaterials;

    // Метод Start викликається один раз перед оновленням першого кадру, 
    // коли об'єкт з'являється на сцені.
    void Start()
    {
        // Запобіжник: якщо ми запустили сцену гри безпосередньо в редакторі Unity, 
        // оминувши Головне меню, GameSettings не буде існувати. 
        // Щоб уникнути помилок, ми просто перериваємо виконання методу.
        if (GameSettings.Instance == null) return;

        // Якщо активовано режим на 4 гравців, ми не беремо кольори з меню
        // (бо поточне меню передає налаштування лише для двох гравців).
        if (GameSettings.Instance.isFourPlayers)
        {
            // Можна нічого не робити, тоді залишаться дефолтні кольори, 
            // які ви вже призначили матеріалам цих фішок в редакторі Unity.
            return;
        }

        // Логіка для 2 гравців (режими PvP / PvE).
        // Визначаємо, який індекс текстури нам потрібен. 
        // Якщо галочка isPlayer1 стоїть - беремо індекс першого гравця, якщо ні - беремо індекс другого.
        int index = isPlayer1 ? GameSettings.Instance.p1TextureIndex : GameSettings.Instance.p2TextureIndex;

        // Перевіряємо, чи отриманий індекс знаходиться в межах нашого масиву матеріалів 
        // (щоб уникнути помилки IndexOutOfRangeException).
        if (index >= 0 && index < allWoodMaterials.Length)
        {
            // Отримуємо компонент Renderer (який відповідає за малювання 3D-об'єкта) 
            // і змінюємо його матеріал на той, який гравець обрав у меню.
            GetComponent<Renderer>().material = allWoodMaterials[index];
        }
    }
}
